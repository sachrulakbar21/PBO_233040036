package pertemuan5;

public class Gelas {
	    private String warna; // Variabel untuk menyimpan warna gelas

	    // Konstruktor untuk menginisialisasi warna gelas
	    public Gelas(String w) {
	        warna = w;
	    }

	    // Setter untuk mengubah warna gelas
	    public void setWarna(String w) {
	        warna = w;
	    }

	    // Getter untuk mendapatkan warna gelas
	    public String getWarna() {
	        return warna;
	    }
	}

